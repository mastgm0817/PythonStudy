# 1. 비교 연산
print("- 비교 연산 문제")
print(2 > 3)  # False
print(15 < 30)  # True
print(1.5 >= 0)  # True
print(3 <= 3)  # True
print("팙팙팙" == "팙팗팗")  # False
print("11111111111111" != "1111111111111")  # False


# 2. 논리 연산
print("- 논리 연산 문제")
print(4 < 6 and 10 >= 10)  # True AND True -> True
print("포기하지말아요" != "포기하지말아요" or "나는 할 수 있다" ==
      "나는 할 수 있다")  # False OR True -> True
print(not 5 == 5)  # Not True -> False


# 3. 맴버쉽 연산
print("- 맴버쉽 연산 문제")
print("a" in "abc")  # a라는 문자열이 abc에 포함되어있나? -> True
print("d" not in "abc")  # d라는 문자열이 abc에 포함되어있지 않다면 -> True
