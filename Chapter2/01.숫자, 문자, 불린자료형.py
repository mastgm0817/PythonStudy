# 숫자 자료형

# 1. 정수형 : 소수점이 없는 수
print(1, 3, 0, -1)
print(1, 3, 0, -1,  sep="")  # 숫자마다 스페이스 간격을 없애고 싶을때
# 2. 실수형: 소수점이 있는 수

print(1.1, 3.1, 0, -1.4)


# 문자열 자료형
print("파이썬 너무 재밌다")
print('파이썬 정말 재밌자너!')
print("'개 짖는 소리좀 안나게 해라' 라고 그는 외쳤다.")

print("파이썬 너무 재밌다. ", end='')  # 자동 줄바꿈을 없애기 위해서는 end=''를 사용하면 된다.
print('파이썬 정말 재밌자너!')
print("'개 짖는 소리좀 안나게 해라' 라고 그는 외쳤다.")

